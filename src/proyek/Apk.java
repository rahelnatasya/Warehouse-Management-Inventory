    private void cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariActionPerformed
    try {
        // Membuat koneksi ke database
        java.sql.Connection conn = proyek.Koneksi.koneksiDB();
        
        // Query SQL dengan parameter
        String sql = "SELECT * FROM tbdata WHERE kodebarang = ? OR namabarang = ?";
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, cari.getText());
        pst.setString(2, cari.getText());
        
        // Menjalankan query
        ResultSet rs = pst.executeQuery();
        
        // Mengecek apakah data ditemukan
        if (rs.next()) {
            kodebarang.setText(rs.getString("kodebarang"));
            namabarang.setText(rs.getString("namabarang"));
            kategoribarang.setSelectedItem(rs.getString("kategoribarang"));
            jumlahstok.setText(rs.getString("jumlahstok"));
            lokasi.setText(rs.getString("lokasi"));
            
            JOptionPane.showMessageDialog(null, 
                "Data tbdata : " + cari.getText() + " ditemukan.\nApakah Anda ingin mengedit atau menghapus?");
        } else {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan.");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
        e.printStackTrace();
    } finally {
        // Mengembalikan fokus ke input pencarian
        cari.requestFocus();
}
    }//GEN-LAST:event_cariActionPerformed

