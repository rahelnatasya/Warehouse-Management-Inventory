    private void tb_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tb_editActionPerformed
    try {
    // Membuat koneksi ke database
    java.sql.Connection conn = proyek.Koneksi.koneksiDB();
    
    // Query SQL untuk update
    String sql = "UPDATE tbdata SET kodebarang=?, namabarang=?, kategoribarang=?, jumlahstok=?, lokasi=?, WHERE kodebarang=?";
    java.sql.PreparedStatement pst = conn.prepareStatement(sql);
    
    // Mengatur nilai untuk parameter
    pst.setString(1, kodebarang.getText());
    pst.setString(2, namabarang.getText());
    pst.setString(3, kategoribarang.getSelectedItem().toString());
    pst.setInt(4, Integer.parseInt(jumlahstok.getText())); 
    pst.setString(5, lokasi.getText());
    
    // Menjalankan perintah update
    pst.executeUpdate();
    
    JOptionPane.showMessageDialog(null, "Data berhasil diperbarui.");
    tampil_data(); // Memperbarui tampilan data
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(null, "Format nomor tidak valid: " + e.getMessage());
} catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Proses edit data gagal: " + e.getMessage());
    e.printStackTrace();
}

    }//GEN-LAST:event_tb_editActionPerformed

    private void tb_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tb_hapusActionPerformed
    try {
    // Membuat koneksi ke database
    java.sql.Connection conn = proyek.Koneksi.koneksiDB();
    
    // Query SQL untuk delete
    String sql = "DELETE FROM tbdata WHERE kodebarang=?";
    java.sql.PreparedStatement pst = conn.prepareStatement(sql);
    
    // Mengatur nilai parameter
    pst.setInt(1, Integer.parseInt(kodebarang.getText()));
    
    // Menjalankan perintah delete
    int rowsDeleted = pst.executeUpdate();
    
    if (rowsDeleted > 0) {
        JOptionPane.showMessageDialog(null, "Data berhasil dihapus.");
        tampil_data(); // Memperbarui tampilan data
    } else {
        JOptionPane.showMessageDialog(null, "Data tidak ditemukan untuk dihapus.");
    }
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(null, "Format nomor tidak valid: " + e.getMessage());
} catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Proses penghapusan gagal: " + e.getMessage());
    e.printStackTrace();
}

    }//GEN-LAST:event_tb_hapusActionPerformed

    private void tb_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tb_simpanActionPerformed
    try {
        // Validasi jumlah stok
        int jumlahStok;
        try {
            jumlahStok = Integer.parseInt(jumlahstok.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Jumlah stok harus berupa angka.");
            return;
        }

        // Query SQL yang benar
        String sql = "INSERT INTO tbdata (kodebarang, namabarang, kategoribarang, jumlahstok, lokasi) VALUES (?, ?, ?, ?, ?)";
        java.sql.Connection conn = proyek.Koneksi.koneksiDB();
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        
        // Mengisi parameter query
        pst.setString(1, kodebarang.getText());
        pst.setString(2, namabarang.getText());
        pst.setString(3, kategoribarang.getSelectedItem().toString());
        pst.setInt(4, jumlahStok);
        pst.setString(5, lokasi.getText());
        
        // Eksekusi query
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "Berhasil disimpan");
        tampil_data();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal disimpan: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_tb_simpanActionPerformed

